package com.Basic;

public class Switchcase {

	public static void main(String[] args) {
    int num = 20;
    switch(num)
    {
    	case 10:
    		System.out.println("the value is 10");
    		break;
    	case 20:
    		System.out.println("the value is 20");
    		break;
        default:
    		System.out.println("not fund");
    }		
    		

    
	}

}
