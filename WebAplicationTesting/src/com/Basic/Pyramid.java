package com.Basic;

public class Pyramid {

	public static void main(String[] args) 
	{
	   for(int i=1;i<=3;i++)
	   {
		   for(int j=1;j<=i;j++)
		   {  
           
			  System.out.print(i);
		   }
		 System.out.println("");  
	   }
	   
	   for(int i=1;i<=3;i++)
	   {
		   for(int j=1;j<=i;j++)
		   {  
           
			  System.out.print("*");
		   }
		 System.out.println("");  
	   }
	   
	   for(int i=1;i<=3;i++)
	   {
		   for(int j=1;j<=i;j++)
		   {  
           
			  System.out.print("1");
		   }
		 System.out.println("");  
	   }
	}
}
	   
  
